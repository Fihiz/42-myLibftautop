#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	const char	*str;
	size_t		i;

	i = 0;
	str = s;
	while (str[i] && i < n)
	{
		if (i == (unsigned char)c)
			return ((void*)str);
		i++;
	}
	return (NULL);
}
